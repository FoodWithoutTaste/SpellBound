	public static float GetNoiseOnSphere(float x, float y, float z,float scale)
	{
		// Normalize the point to lie on the unit sphere
		float r = Mathf.Sqrt(x * x + y * y + z * z);
		x /= r;
		y /= r;
		z /= r;

		// Convert Cartesian coordinates to spherical coordinates
		float theta = Mathf.Atan2(y, x);  // azimuthal angle, range: [-π, π]
		float phi = Mathf.Acos(z);        // polar angle, range: [0, π]

		// Map spherical coordinates to 2D noise plane
		float u = (theta + Mathf.PI) / (2 * Mathf.PI);
		float v = phi / Mathf.PI;
		u *= scale;
		v*= scale;

		// Generate Perlin noise based on these coordinates
		return Mathf.PerlinNoise(u, v);
	}
	public static Vector2 Map3DTo2D(Vector3 pos, Vector3 center)
	{
		// Convert (x, y, z) to cylindrical coordinates
		float r = Mathf.Sqrt(Mathf.Pow(pos.x - center.x, 2) + Mathf.Pow(pos.z - center.z, 2));
		float y = pos.y - center.y; // y coordinate relative to the center
		return new Vector2(r, y);
	}
	public static float CalculateContinentalness(Vector3 pos, PlanetGeneration planet)
    {

		float continentalnessValue = 0;
	
		float a = planet.continentalnessScale;
		float b = planet.continentalnessAplitude;
		float temp = PerlinNoise3D((pos.x ) * a, (pos.y ) * a, (pos.z ) * b);

		for (int i = 0; i < planet.continentalRanges.Length; i++)
        {
		   if(temp < planet.continentalRanges[i].toRange && temp > planet.continentalRanges[i].fromRange)
            {
				continentalnessValue = planet.continentalRanges[i].continentalnessValue;
            }
        }

		return PerlinNoise3d((pos.x) * a, (pos.y) * a, (pos.z) * b); 

	}

	public static float GetTerrainValue(Vector3 pos,PlanetGeneration planet)
	{
		int cx = planet.worldSizeInChunks * planet.chunkSize.x / 2;
		int cy = planet.worldSizeInChunks * planet.chunkSize.y / 2;
		int cz = planet.worldSizeInChunks * planet.chunkSize.x / 2;
		int radius = planet.worldSizeInChunks * planet.chunkSize.x / 2 - 1;

		float distance = Mathf.Sqrt(Mathf.Pow(pos.x - cx, 2) + Mathf.Pow(pos.y - cy, 2) + Mathf.Pow(pos.z - cz, 2));



		
	

		// Calculate te Perlin noise value using spherical coordinates
		float noiseScale = planet.continentalnessScale; // Adjust this to control the frequency of the noise
		float noiseValue = GetPerlinLayers(planet.noiseScale, planet.amplitude, planet.layers, planet.descaler, planet.upscaler, pos);




		// Calculate Perlin noise using spherical coordinates
		float continentalnessValue = CalculateContinentalness(pos, planet);


		float terreinValue = Mathf.Clamp01(distance / radius) - Mathf.Abs(continentalnessValue) * planet.continentalnessAplitude - noiseValue;


		return Mathf.Clamp01(terreinValue);
        
		
	}

	public static float GetPerlinLayers(float noiseScale,float amplitude,int layers,float descaler,float upscaler,Vector3 pos)
    {
		float noiseValue = 0;
		float a = noiseScale;
		float b = amplitude;


		for (int i = 0; i < layers; i++)
		{
			noiseValue += PerlinNoise3D((pos.x + 0.5f) * a, (pos.y + 0.5f) * a, (pos.z + 0.5f) * a) * b;
			b *= descaler;
			a *= upscaler;
		}
		return noiseValue;
	}
