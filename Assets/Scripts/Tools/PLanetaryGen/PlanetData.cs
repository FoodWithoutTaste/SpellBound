	public static bool IsChunkInFrustum(Camera cam, Vector3 chunkPos, Vector3 chunkSize, float buffer)
	{
		// Get the frustum planes
		Plane[] frustumPlanes = GeometryUtility.CalculateFrustumPlanes(cam);

		// Adjust frustum planes to include buffer
		for (int i = 0; i < frustumPlanes.Length; i++)
		{
			Vector3 normal = frustumPlanes[i].normal;
			float distance = frustumPlanes[i].distance;
			frustumPlanes[i] = new Plane(normal, distance + buffer);
		}

		// Define the chunk's bounding box
		Bounds chunkBounds = new Bounds(chunkPos, chunkSize/2);

		// Check if the bounding box intersects with the frustum planes
		return GeometryUtility.TestPlanesAABB(frustumPlanes, chunkBounds);
	}
